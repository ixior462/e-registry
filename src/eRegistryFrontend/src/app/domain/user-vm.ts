/**
 * Represents user
 * @export
 * @class UserVM
 */
export class UserVM {
  /**
   * User name
   * @type {string}
   * @memberof UserVM
   */
  public name: string;
  /**
   * User surname
   * @type {string}
   * @memberof UserVM
   */
  public surname: string;
  /**
   * User login
   * @type {string}
   * @memberof UserVM
   */
  public login: string;
  /**
   * Shows whether user is selected or not
   * @memberof UserVM
   */
  public selected: boolean;
}
